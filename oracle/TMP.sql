COMMENT ON COLUMN BULLETIN.RE_BULT_DATE IS '(후기게시판)작성일자';
COMMENT ON COLUMN BULLETIN.RE_BULT_WRITER IS '(후기게시판)작성자';
COMMENT ON COLUMN BULLETIN.RE_BULT_CONT IS '(후기게시판)내용';
COMMENT ON COLUMN BULLETIN.BULT_VIEW IS '(후기게시판)조회수';;;
COMMENT ON COLUMN BULLETIN.RE_BULT_REPLY IS '(후기게시판)댓글수';
